    private void btsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsalvarActionPerformed
        // TODO add your handling code here:
        Border borda;
        borda = BorderFactory.createLineBorder(Color.white,2);
        pfsenha.setBorder(borda);
        if (pfsenha.getText().contentEquals(pfnovasenha.getText())){
            int rs;
            String sql,senha;
            senha = pfsenha.getText();
            try {
                sql = "update HE_EMPRESAS set senha = ? where email = ?";
               // System.out.println(sql);
                PreparedStatement stm = con.prepareStatement(sql);
                stm.setString(1, String.valueOf(senha.hashCode()));
                stm.setString(2, conexao.email);
                rs = stm.executeUpdate();
                if (rs>0) {
                    JOptionPane.showMessageDialog(null, "Dados Gravados com Sucesso!");
                } 
                else {
                    JOptionPane.showMessageDialog(null, "Erro ao Gravar!");
                }
            } 
            catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro! "+ e);
            }
        }
        else{
            borda = BorderFactory.createLineBorder(Color.red,2);
            pfsenha.setBorder(borda);
            pfsenha.requestFocus();
            pfsenha.setText("");
            jLabel7.setText("As senhas n칚o correspondem!");
            jLabel7.setVisible(true);
        }
    }//GEN-LAST:event_btsalvarActionPerformed

